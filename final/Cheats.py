def cheat(keys, flags):
    for i in range(len(cheats)):
        if keys[cheats[i][1][cheats[i][0]]]:
            cheats[i][0] += 1
            if cheats[i][0] == len(cheats[i][1]):
                move = True if i % 2 == 0 else False
                if i > 5:
                    flags = [move] * 3
                else:
                    flags[i // 2] = move
                cheats[i][0] = 0
                break
        else:
            cheats[i][0] = 0

    return flags
